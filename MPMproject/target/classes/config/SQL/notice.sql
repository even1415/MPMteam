/* 공지사항 */
CREATE TABLE NOTICE (
	NIDX NUMBER(8) NOT NULL, /* 공지번호 */ 
	NTITLE VARCHAR2(50) NOT NULL, /* 공지제목 */
	NWRITER VARCHAR2(50) NOT NULL, /* 공지작성자 */
	NCONTENT VARCHAR2(500), /* 공지내용 */
	FILENAME VARCHAR2(300), //첨부파일명
	ORIGINFILENAME VARCHAR2(300),//원본파일명
	FILESIZE NUMBER(8),//첨부파일 크기
	NDATE DATE NOT NULL /* 공지등록일 */
);

COMMENT ON TABLE NOTICE IS '공지사항';

COMMENT ON COLUMN NOTICE.NIDX IS '공지번호';

COMMENT ON COLUMN NOTICE.NTITLE IS '공지제목';

COMMENT ON COLUMN NOTICE.NWRITER IS '공지작성자';

COMMENT ON COLUMN NOTICE.NCONTENT IS '공지내용';

COMMENT ON COLUMN NOTICE.NREADNUM IS '공지조회수';

COMMENT ON COLUMN NOTICE.NDATE IS '공지등록일';

