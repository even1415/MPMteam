/* 음원 */
CREATE TABLE MUSIC (
	MIDX NUMBER(8) NOT NULL, /* 음원번호 */
	MTITLE VARCHAR2(50) NOT NULL, /* 음원타이틀 */
	MCOMENT VARCHAR2(200), /* 음원코멘트 */
	Mlyrics VARCHAR2(4000), /* 음원가사 */
	MDATE DATE NOT NULL, /* 음원등록일 */
	MREADNUM NUMBER(8) NOT NULL, /* 음원조회수 */
	FILENAME VARCHAR2(1000) NOT NULL, /* 첨부파일 */
	FILESIZE NUMBER(12) NOT NULL, /* 파일크기 */
	PLAYTIME NUMBER(8) NOT NULL /* 재생시간 */
);

COMMENT ON TABLE MUSIC IS '음원';

COMMENT ON COLUMN MUSIC.MIDX IS '음원번호';

COMMENT ON COLUMN MUSIC.MTITLE IS '음원타이틀';

COMMENT ON COLUMN MUSIC.MCOMENT IS '음원코멘트';

COMMENT ON COLUMN MUSIC.Mlyrics IS '음원가사';

COMMENT ON COLUMN MUSIC.MDATE IS '음원등록일';

COMMENT ON COLUMN MUSIC.MREADNUM IS '음원조회수';

COMMENT ON COLUMN MUSIC.FILENAME IS '첨부파일';

COMMENT ON COLUMN MUSIC.FILESIZE IS '파일크기';

COMMENT ON COLUMN MUSIC.PLAYTIME IS '재생시간';

