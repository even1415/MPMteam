/* 회원 */
DROP TABLE MEMBER 
	CASCADE CONSTRAINTS;

/* 회원 */
CREATE TABLE MEMBER (
	IDX NUMBER(8) NOT NULL, /* 회원번호 */
	NICKNAME VARCHAR2(30) NOT NULL, /* 회원이름 */
	USERID VARCHAR2(50) NOT NULL, /* 아이디 */
	EMAIL VARCHAR2(100), /* 이메일 */
	PWD VARCHAR2(30) NOT NULL /* 비밀번호 */
);

COMMENT ON TABLE MEMBER IS '회원';

COMMENT ON COLUMN MEMBER.IDX IS '회원번호';

COMMENT ON COLUMN MEMBER.NICKNAME IS '회원이름';

COMMENT ON COLUMN MEMBER.USERID IS '아이디';

COMMENT ON COLUMN MEMBER.EMAIL IS '이메일';

COMMENT ON COLUMN MEMBER.PWD IS '비밀번호';

CREATE UNIQUE INDEX PK_MEMBER
	ON MEMBER (
		IDX ASC
	);

ALTER TABLE MEMBER
	ADD
		CONSTRAINT PK_MEMBER
		PRIMARY KEY (
			IDX
		);